    private void jButton_startActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_startActionPerformed
        try {
            if (ProgramControler.DBSettings.getMSQLite() == null) {
                throw new Exception(ProgramControler.DBSettings.exc_connectToDataBase);
            }

            if (jButton_start.getText().equals("start")) {

                mFastaExportAndAlignment = new FastaExportAndAlignment(ProgramControler, jLabel_statusLabel, this);
                this.InitialSettings();
                mFastaExportAndAlignment.StartProcessing();
                SetsForProcessing(true);

            } else if (jButton_start.getText().equals("stop")) {

                mFastaExportAndAlignment.interrupt();
            }

        } catch (Exception ex) {
            Logger.getLogger(DBSettings.loggerProgram).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this,
                    ex.getMessage(), this.getName(), JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton_startActionPerformed

    private void InitialSettings() throws Exception {
        try {

            //Data base settings
            if (this.ProgramControler.DBSettings == null || this.ProgramControler.DBSettings.getMSQLite() == null) {
                throw new Exception("Connect to data base");
            }

            folderPath = this.jTextField_muscleOutput.getText();
            if (folderPath == null || folderPath.isEmpty()) {
                throw new Exception("Set output folder!");
            }
            mFastaExportAndAlignment.setFolderPath(folderPath);

            //output name stuff:
            List<Object> nameOptions = new ArrayList<Object>();
            if (!jComboBox_name1.getSelectedItem().toString().isEmpty()) {
                nameOptions.add(jComboBox_name1.getSelectedItem());
            }
            if (!jComboBox_name2.getSelectedItem().toString().isEmpty()) {
                nameOptions.add(jComboBox_name2.getSelectedItem());
            }
            if (!jComboBox_name3.getSelectedItem().toString().isEmpty()) {
                nameOptions.add(jComboBox_name3.getSelectedItem());
            }
            if (!jComboBox_name4.getSelectedItem().toString().isEmpty()) {
                nameOptions.add(jComboBox_name4.getSelectedItem());
            }
            mFastaExportAndAlignment.setNameOptions(nameOptions);

            mFastaExportAndAlignment.setPrefix(this.jTextField_prefix.getText());
            if (mFastaExportAndAlignment.getPrefix() != null && nameOptions.isEmpty()) {
                throw new Exception("Set at least one piece of file name");
            }

            String minL = this.jTextField_minLength.getText();
            if (minL != null && !minL.isEmpty()) {
                try {
                    minLength = Integer.parseInt(minL.trim());
                    mFastaExportAndAlignment.setMinLength(minLength);
                } catch (NumberFormatException nfe) {
                    System.out.println("NumberFormatException: " + nfe.getMessage());
                    throw nfe;
                }
            }

            String maxL = this.jTextField_maxLength.getText();
            if (maxL != null && !maxL.isEmpty()) {
                try {
                    maxLength = Integer.parseInt(maxL.trim());
                    mFastaExportAndAlignment.setMaxLength(maxLength);
                } catch (NumberFormatException nfe) {
                    System.out.println("NumberFormatException: " + nfe.getMessage());
                    throw nfe;
                }
            }

            //test for length range:
            if (minL != null && !minL.isEmpty() && maxL != null && !maxL.isEmpty()) {
                if (minLength > maxLength) {
                    throw new Exception("Minimal length cannot be bigger than maxlength");
                }
            }

            Dictionary MuscleOpt_dic = new Hashtable();
            if (jCheckBox_alignment.isSelected()) {
                muscleFile = prefs.get("MUSCLE_FILE", null);
                if (muscleFile == null || muscleFile.isEmpty()) {
                    throw new Exception("Set Muscle program path in project window");
                }
                mFastaExportAndAlignment.setMuscleFile(muscleFile);
                //mFastaExportAndAlignment.setMuscleOption(jTextField_muscleOptions.getText());

                MuscleOpt_dic.put(MuscleEnum.noanchors, !jCheckBox_anchors.isSelected());
                MuscleOpt_dic.put(MuscleEnum.brenner, jCheckBox_brenner.isSelected());
                //MuscleOpt_dic.put(MuscleEnum.cluster, jCheckBox_cluster.isSelected());
                MuscleOpt_dic.put(MuscleEnum.diags, jCheckBox_diags.isSelected());
                MuscleOpt_dic.put(MuscleEnum.dimer, jCheckBox_dimer.isSelected());

                mFastaExportAndAlignment.setMuscleOption(MuscleOpt_dic);
            }

            //minimal number of sequences:
            try {
                int minNumberOfSeq = 1;
                minNumberOfSeq = Integer.parseInt(jTextField_minNumSeq.getText());
                mFastaExportAndAlignment.setMinNumberOfSeq(minNumberOfSeq);
            } catch (Exception e) {
            }

//            mFastaExportAndAlignment.setUseFile(jCheckBox_file.isSelected());
//            mFastaExportAndAlignment.setUseTag(jCheckBox_tag.isSelected());
//            mFastaExportAndAlignment.setUseSpecies(jCheckBox_species.isSelected());
//            mFastaExportAndAlignment.setUseSeqNumber(jCheckBox_seqNmber.isSelected());



            //minimal variant count:
            try {
                int minVarianNumber = 1;
                minVarianNumber = Integer.parseInt(jTextField_variantCount.getText());
                mFastaExportAndAlignment.setMinVarianCount(minVarianNumber);
            } catch (Exception e) {
            }


            //alignment
            mFastaExportAndAlignment.setAlignment(jCheckBox_alignment.isSelected());

            //reorder align:
            mFastaExportAndAlignment.setReOrderAlign(jCheckBox_sortOrignialFasta.isSelected());


        } catch (Exception e) {
            throw e;
        }
    }

