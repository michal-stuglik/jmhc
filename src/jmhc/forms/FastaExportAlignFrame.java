    private void jButton_muscleOutputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_muscleOutputActionPerformed

        JFileChooser chooser = null;
        try {

            chooser = new JFileChooser();
            chooser.setMultiSelectionEnabled(false);
            chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            String lastDir = this.prefs.get("LAST_OUTPUT_DIR", null);
            if (lastDir != null && !lastDir.isEmpty()) {
                chooser.setCurrentDirectory(new File(lastDir));
            }

            int result = chooser.showOpenDialog(null);
            if (result == JFileChooser.APPROVE_OPTION) {
                File file = chooser.getSelectedFile();
                if (!file.exists()) {
                    throw new Exception("no such file");
                }

                PathChecker pc = new PathChecker(file.getAbsolutePath());
                if (!pc.Check()) {
                    this.jTextField_muscleOutput.setText("");
                    throw new Exception(pc.getMessage());
                }

                this.jTextField_muscleOutput.setText(file.getAbsolutePath());
                prefs.put("LAST_OUTPUT_DIR", jTextField_muscleOutput.getText());
            } else {
            }

        } catch (Exception ex) {
            Logger.getLogger(DBSettings.loggerProgram).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this,
                    ex.getMessage(), this.getName(), JOptionPane.ERROR_MESSAGE);
        } finally {
        }
}//GEN-LAST:event_jButton_muscleOutputActionPerformed
